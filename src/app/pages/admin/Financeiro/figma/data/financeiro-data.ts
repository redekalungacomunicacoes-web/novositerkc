export const formatCurrency = (value: number) => {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL',
  }).format(value);
};

export const formatDate = (date: string) => {
  return new Date(date).toLocaleDateString('pt-BR');
};

export const formatPercent = (value: number) => {
  return `${value}%`;
};
